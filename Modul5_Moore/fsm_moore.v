//
//         w=1          w=1         w=1
//   в”Ңв”Җв”Җв”җ в”Җв”Җв”Җв”Җ в”Ңв”Җв”Җв”җ в”Җв”Җв”Җв”Җ в”Ңв”Җв”Җв”җ в”Җв”Җв”Җв”Җ в”Ңв”Җв”Җв”җ
//   в”ӮS0в”Ӯ      в”ӮS0в”Ӯ      в”ӮS0в”Ӯ      в”ӮS0в”Ӯ   (reset goes to S0)
//   в””в”Җв”Җв”ҳ      в””в”Җв”Җв”ҳ      в””в”Җв”Җв”ҳ      в””в”Җв”Җв”ҳ
//
//   RESET
//     в”Ӯ
//     в–ј
//   в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ  w=0   в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ  w=1   в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ  w=0   в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
//   в”Ӯ  S0    в”Ӯв”Җв”Җв”Җв”Җв”Җв”Җв”Җв–әв”Ӯ  S1    в”Ӯв”Җв”Җв”Җв”Җв”Җв”Җв”Җв–әв”Ӯ  S2    в”Ӯв”Җв”Җв”Җв”Җв”Җв”Җв”Җв–әв”Ӯ  S3    в”Ӯ
//   в”Ӯ y = 0  в”Ӯ        в”Ӯ y = 0  в”Ӯ        в”Ӯ y = 0  в”Ӯ        в”Ӯ y = 1  в”Ӯ
//   в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ        в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ        в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ        в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
//     в–І  в”Ӯ w=1          в”Ӯ  в–І w=0          в”Ӯ w=1              в”Ӯ w=0   в”Ӯ w=1
//     в”Ӯ  в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ  в”Ӯ              в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ       в”Ӯ
//     в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
//       (S3 w=0 вҶ’ S1; S3 w=1 вҶ’ S0)
//
// в”Җв”Җ TRUTH TABLE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//   Current  в”Ӯ Next State  в”Ӯ Output
//   State    в”Ӯ w=0  в”Ӯ w=1  в”Ӯ  y
//  в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җ
//   S0 (00)  в”Ӯ  S1  в”Ӯ  S0  в”Ӯ  0
//   S1 (01)  в”Ӯ  S1  в”Ӯ  S2  в”Ӯ  0
//   S2 (10)  в”Ӯ  S3  в”Ӯ  S0  в”Ӯ  0
//   S3 (11)  в”Ӯ  S1  в”Ӯ  S0  в”Ӯ  1
//
// в”Җв”Җ K-MAP Q2next в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//          W=0  W=1
//  Q2Q1=00:  0    0
//  Q2Q1=01:  0    1   вҶҗ group A: wВ·QМ„2В·Q1
//  Q2Q1=11:  0    0
//  Q2Q1=10:  1    0   вҶҗ group B: wМ„В·Q2В·QМ„1
//
//  Q2next = wВ·QМ„2В·Q1 + wМ„В·Q2В·QМ„1
//
// в”Җв”Җ K-MAP Q1next в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//          W=0  W=1
//  Q2Q1=00:  1    0
//  Q2Q1=01:  1    0   вҶҗ all W=0 cells = 1
//  Q2Q1=11:  1    0
//  Q2Q1=10:  1    0
//
//  Q1next = wМ„
//
// в”Җв”Җ OUTPUT в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//  y = Q2В·Q1
// =============================================================
module fsm_moore(
    input  wire        clk,
    input  wire        reset,
    input  wire        ce,            // clock-enable from clock_divider
    input  wire        w,             // input from SW0
    output reg         y,             // Moore output
    output wire [1:0]  state_display  // for 7-segment
);

    // State encoding
    parameter S0 = 2'b00,   // Initial: no match
              S1 = 2'b01,   // Received "0"
              S2 = 2'b10,   // Received "01"
              S3 = 2'b11;   // Received "010" вҖ” DETECTED

    reg [1:0] curr, next;

    assign state_display = curr;

    // в”Җв”Җ Sequential: state register в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    always @(posedge clk or posedge reset) begin
        if (reset)    curr <= S0;
        else if (ce)  curr <= next;
    end

    // в”Җв”Җ Combinational: next-state + output logic в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    always @(*) begin
        // Moore: output is a function of current state ONLY
        y = (curr == S3);  // y = Q2В·Q1

        case (curr)
            S0: next = (w) ? S0 : S1;  // looking for first '0'
            S1: next = (w) ? S2 : S1;  // got '0', now need '1'
            S2: next = (w) ? S0 : S3;  // got '01', now need '0'
            S3: next = (w) ? S0 : S1;  // detected вҖ” restart
            default: next = S0;
        endcase
    end

endmodule
